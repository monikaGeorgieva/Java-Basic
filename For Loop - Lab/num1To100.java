public class demo {
    public static void main(String[] args) {
        //for цикъл - повтаряме едно и също действие определен
        //брой пъти
        // 1 до 100
        // 1.начало - 1
        // 2.край - 100
        // повтаряме: отпечатвам числото
        // промяната : +1
       for (int a = 1; a <= 100 ; a++){
           System.out.println(a);
        }
    }
}
